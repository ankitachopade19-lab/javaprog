package MyPackage;

public class RelationalOperator {
	
	public static void main(String[] args) {

		// Relational Operator
		// > , < , >= , <= , == , !=

		int a = 15, b = 20 ;

		System.out.println(a + " is Greater than " + b + "  - " + (a > b));
		System.out.println(a + " is less than " + b + " - " + (15 < 20));

		System.out.println("4 is Greater than equal to 12 " + " - "  + (4 >= 12)); 
		System.out.println(11 <= 30); 

		System.out.println(a == b); 
		System.out.println("18 is not equal to 19 " + "-" + (18 != 19));

	}
  

}
