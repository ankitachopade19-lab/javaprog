package MyPackage;

public class ArithmeticOperator {
	
	public static void main(String args[]){
		
	int a = 30;
	int b = 10;
	
	int c = a + b;

	System.out.println("Addition of " + a + " + " + b + " = " + c); 
	System.out.println("Substaction of " + a + " - " + b + " = " + (a - b)); 
	System.out.println("Multification of " + a + " * " + b + " = " + (a * b)); 
	System.out.println("Division of " + a + " / " + b + " = " + (c / b) + " Quotient"); 
	System.out.println("Modulas of " + a + " % " + b + " = " + (c % b) + " Remainder"); 

  }

}


